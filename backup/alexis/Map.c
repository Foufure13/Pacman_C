Map* CreateMap() {
    Map *map = malloc(sizeof(Map));
    map->wall = LoadTexture("images/brick/Bricks.png");
    map->cornerwall = LoadTexture("images/cornerwall.png");

    map->src.x = 0;
    map->src.y = 0;
    map->src.w = 32;
    map->src.h = 32;

    map->dest.x = 0;
    map->dest.y = 0;
    map->dest.w = 32;
    map->dest.h = 32;


    LoadMap(map,lvl1);
    return map;
}

void LoadMap(Map* map, int arr[26][29])
{
    for (int row = 0; row < 26; row++)
    {
        for (int column = 0; column < 29; column++)
        {
            map->map[row][column] = arr[row][column];
        }
    }
}

void DrawMap(Map* map)
{
    int type = 0;

    for (int row = 0; row < 26; row++)
    {
        for (int column = 0; column < 29; column++)
        {
            type = map->map[row][column];

            map->dest.x = column * 32;
            map->dest.y = row * 32;

            switch (type)
            {
            case 1:
                DrawTexture(map->wall, map->src, map->dest);
                break;
            case 2:
                DrawTexture(map->cornerwall, map->src, map->dest);
                break;
        
            default:
                break;
            }
        }
    }
}